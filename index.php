				<h2>思考能力</h2>
				<h4>卢柏红/2015/07/19</h4>
				<img src="image/article_bg.jpg" height="126" width="171"/>
				<p>“善于思考”是应届生简历上的高频词汇，但在实践中，具备独立思考能力的应届生少之又少。职场中要求的“思考能力”并不是让你进入哲学领域去思考历史走向、人文变迁、岁月流逝，而是当你在工作中遇到问题时，能尝试想出解决方法。很多应届生在工作中遇到一丁点问题，想都不想张口就问。职场不是学校，主管更不是老师，老师的专职工作就是为你答疑解惑，可主管也有自己的活要做。</p><p>影响双方工作效率的同时，更不利于自身提。
				如果你想过还找不到解决思路，便可和主管交流。把你的思考路径、困惑说出来，让他为你答疑解惑。这样便容易看出自己在工作方法、思考方式上有哪些误区，也才更有利于自身提高职场永远是团队在一起战斗，既然身处于团队，就要学会和同事沟通协作。
				率低下不说，信息也会有偏差。。</p>
