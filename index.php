				<h3>主动性</h3>
				<h4>卢柏红/2015/07/19</h4>
				<img src="image/article_bg2.jpg" height="126" width="171"/>
				<p>“主动性强”也是高频词汇，但在实践中，具备主动性的应届生还是少之又少。
				应届生很容易陷入“推一步走一步”的境地，手上的工作做完后却不主动和上司沟通。很多时候给你工作的时候他给出的时间都很宽松，如果你提前做完，一定要主动反馈，让他及时安排。工作中还有一个很常见的场景：</p><p>上司给活的时候可能只想了两分钟，他交代的要点并不全。这时候你在完成基本点的时候，一定要主动想一下是否能做的更好？
				基本完成并不能提升你的形象和能力，超出预期才能给人惊喜！在互联网这么发达的今天，基础工作中遇到的难点在网上早已有了成型的解决方法。如果你有一定的信息收集能力去自学的话，不论是对工作，还是对自我提升都有极大帮助。
				</p>
